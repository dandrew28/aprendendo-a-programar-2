// fazer tabuada de 10

let marcador = 1
let m = 20
let variavel = 100

while (marcador <= m) {
    let resultado = marcador * variavel // tem que ficar aqui dentro para que a condição while (1 atá m) seja válida.
        
    console.log (`${marcador} x ${variavel} = ${resultado}`)
   marcador = marcador + 1
}